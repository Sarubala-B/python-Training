# Concatenation:
first_name = "John"
last_name = "Doe"
full_name = first_name + " " + last_name
print(full_name)

# Repetition:
repeated = "Hello " * 3
print(repeated)  

# Length:
text = "Python"
print(len(text))  

# Case conversion:
text = "Hello"
print(text.lower())  
print(text.upper()) 

# Splitting & Joining:
sentence = "This is a test"
words = sentence.split()  
print(words)  

joined_sentence = "-".join(words)
print(joined_sentence)  

# Stripping:
text = "  Hello World  "
print(text.strip())  

# Substring Check:
print("test" in "This is a test")  

# Slicing:
text = "Python"
print(text[0:3])  
print(text[-3:])  


# repr() Function
x = 10
print(repr(x))  

name = "Alice"
print(repr(name))  

# str() Function
x = 10
print(str(x))  

name = "Alice"
print(str(name)) 
